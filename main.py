if not os.path.exists(REPORT_DIR):
    os.makedirs(REPORT_DIR)

# DATABASE SETUP 
def init_db():
    conn = sqlite3.connect('scans.db')
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS scans (
            id TEXT PRIMARY KEY,
            timestamp TEXT,
            filename TEXT,
            results_json TEXT
        )
    ''')
    conn.commit()
    conn.close()

init_db()

# PDF GENERATOR
class PDFReport(FPDF):
    def header(self):
        self.set_font('Arial', 'B', 16)
        self.cell(0, 15, 'SentryVault Security Audit Report', 0, 1, 'C')
        self.ln(5)

    def footer(self):
        self.set_y(-15)
        self.set_font('Arial', 'I', 8)
        self.cell(0, 10, f'Generated by SentryVault Engine v3.5 - Page {self.page_no()}', 0, 0, 'C')

#UTILS
def get_risk_score(vulns):
    if not vulns: return "Safe"
    for v in vulns:
        if "critical" in str(v).lower(): return "Critical"
    return "Moderate"

def scan_pkg(name, version, ecosystem):
    # Display logic for missing versions
    display_version = version if (version and version != "0.0.0") else "Not Specified in your file"
    search_version = version if (version and version != "0.0.0") else "0.0.0"

    url = "https://api.osv.dev/v1/query"
    payload = {"version": search_version, "package": {"name": name, "ecosystem": ecosystem}}
    try:
        res = requests.post(url, json=payload, timeout=10)
        if res.status_code == 200:
            data = res.json()
            vulns = data.get("vulns", [])
            risk = get_risk_score(vulns)
            fix_cmd = f"pip install {name} --upgrade" if ecosystem == "PyPI" else f"npm install {name}@latest"
            return {
                "name": name, "version": display_version, 
                "risk": risk, "count": len(vulns), "type": ecosystem, 
                "fix": fix_cmd if len(vulns) > 0 else "Verified Safe"
            }
    except: return None
    return None

